install.packages("lattice")
library(lattice)
summary(emp_data)
hist(emp_data$Salary_hike)
qqnorm(emp_data$Salary_hike)
qqline(emp_data$Salary_hike)
boxplot(emp_data$Salary_hike)
attach(emp_data)
plot(Salary_hike,Churn_out_rate)
plot(Salary_hike,Churn_out_rate,main="ScatterPlot",col="Dodgerblue4",col.main="Dodgerblue4",xlab="salary hike",ylab="chunout rate",pch=20)
cor(Salary_hike,Churn_out_rate)
reg<-lm(Churn_out_rate~Salary_hike,data=emp_data)
summary(reg)
confint(reg,level=0.95)
predict<-predict(reg,interval="predict")
cor(predict,Churn_out_rate)
reg2<-lm(Churn_out_rate~log(Salary_hike),data=emp_data)
summary(reg2)
confint(reg2,level=0.95)
predict2<-predict(reg2,interval="predict")
cor(predict2,Churn_out_rate)
reg_sqrt<-lm(Churn_out_rate~sqrt(Salary_hike),data=emp_data)
summary(reg_sqrt)
confint(reg_sqrt,level=0.95)
predict3<-predict(reg_sqrt,interval="predict")
cor(predict3,Churn_out_rate)
