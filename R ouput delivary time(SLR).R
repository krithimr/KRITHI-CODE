install.packages("lattice")
library(lattice)
summary(delivery_time)
hist(delivery_time$Sorting.Time)
qqnorm(delivery_time$Sorting.Time)
qqline(delivery_time$Sorting.Time)
boxplot(delivery_time)
attach(delivery_time)
plot(Sorting.Time,Delivery.Time)
plot(Sorting.Time,Delivery.Time,main="ScatterPlot",col="Dodgerblue4",col.main="Dodgerblue4",xlab="sorting time",ylab="delivery time",pch=20)
?plot
cor(Sorting.Time,Delivery.Time)
reg<-lm(Delivery.Time~Sorting.Time,data=delivery_time)
summary(reg)
confint(reg,level=0.95)
predict<-predict(reg,interval="predict")
cor(predict,Delivery.Time)
reg_log<-lm(Delivery.Time~log(Sorting.Time),data=delivery_time)
summary(reg_log)
confint(reg,level=0.95)
predict2<-predict(reg,interval="predict")
cor(predict2,Delivery.Time)
reg_sqrt<-lm(Delivery.Time~sqrt(Sorting.Time),data=delivery_time)
summary(reg_sqrt)
confint(reg,level=0.95)
predict3<-predict(reg,interval="predict")
cor(predict3,Delivery.Time)
